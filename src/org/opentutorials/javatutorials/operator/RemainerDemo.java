package org.opentutorials.javatutorials.operator;

public class RemainerDemo {
    public static void main(String[] args) {

        int a = 3;
        System.out.println(0%a); // 값 : 0
        System.out.println(1%a); // 값 : 1
        System.out.println(2%a); // 값 : 2
        System.out.println(3%a); // 값 : 0
        System.out.println(4%a); // 값 : 1
        System.out.println(5%a); // 값 : 2
        System.out.println(6%a); // 값 : 0
        /**
         * 나머지 계산 연습
         * a 라는 변수엔 3의 값이 다 들어감.
         */
    }
}
