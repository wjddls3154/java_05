package org.opentutorials.javatutorials.operator;

public class ArithmeticDemo {
    public static void main(String[] args) {

        // result 의 값은 1+2 = 3
        int result = 1+2;
        System.out.println(result);

        // result 의 값은 3-1 = 2
        result = result-1;
        System.out.println(result);

        // result 의 값은 2*2 = 4
        result = result * 2;
        System.out.println(result);

        // result 의 값은 4/2 = 2
        result = result / 2;
        System.out.println(result);


        // result 의 값은 2+8 = 10
        result = result + 8;
        System.out.println(result);

        // result 의 값은 10%7 =3
        result = result % 7;
        System.out.println(result);

        /**
         * 산술 연산자 연습
         */
    }

}

