package variables;

public class IntegerDemo {
    public static void main(String[] args) {

        int a;
        a = 1;
        System.out.println(a+1); // 2

        a = 2;
        System.out.println(a+1); // 3

        /**
         * int a; 는 변수를 만드는 구문(변수선언), int 는 데이터 형식(정수), a 는 변수의 이름
         * a 라는 변수에 1을 대입했기 때문에, a+1 의 결과값은 2가 나온다.
         * 또한, 아래에 a 라는 변수에 2 라는 값을 재할당 했기 때문에, a+1 의 결과값은 2가 아닌 3이 나온다.
         * 이까지 변수의 선언과 할당.
         */
    }
}
